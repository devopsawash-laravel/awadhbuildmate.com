     */
    public function store(Request $request)
        {
            $validated = $request->validate([

                'name' => 'required',

                'slug' => 'required|unique:sites',

                'location' => 'required',

                'status' => 'required',
            ]);

            Site::create($request->all());

            return redirect()
                ->route('sites.index')
                ->with(
                    'success',
                    'Site added successfully.'
                );
        }
